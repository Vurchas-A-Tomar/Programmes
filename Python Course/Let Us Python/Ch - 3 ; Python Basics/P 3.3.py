# Convert into Int
print(int(3.14))
a = int('485')
b = int('768')
c = a + b
print(c)
print(int('1101', 2))
print(int('341', 8))
print(int('21', 16))

# Convert tp Float
print(float(35))
i = float('4.85')
j = float('7.68')
k = i + j
print(k)

# Convert to Complex
print(complex(35))
x = complex(4.85, 1.1)
y = complex(7.68, 2.1)
z = x + y
print(z)

# Convert to Bool
print(bool(35))
print(bool(1.2))
print(int(False))
print(int(True))
