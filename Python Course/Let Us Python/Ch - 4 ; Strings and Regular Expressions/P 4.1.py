# Simple Strings
msg1 = 'Hoopla'
print(msg1)

# Strings with Special Characters
msg2 = 'He said, \'Let us Python\'.'
file1 = 'C:\\temp\\newfile'
file2 = r'C:\temp\newfile'
print(msg2)
print(file1)
print(file2)

# Multiline Strings
msg3 = 'What is this life if full of care... /' \
       'We have no time to stand and stare'
msg4 = '''What is this life full of care... 
We have no time to stand and stare'''
print(msg3)
print(msg4)

# String Replication
msg5 = 'Maclearn!'
print(msg5 * 3)

# Immutability of Strings
msg6 = 'Utopia'
msg7 = 'Today!'
msg6 = msg7 + msg6

# Built-in Functions
print(len('Hoopla'))
print(min('Hoopla'))
print(max('Hoopla'))

